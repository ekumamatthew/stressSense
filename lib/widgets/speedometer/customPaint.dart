/**
 * This page controls the speedometer color from red - green depending on the average value
 */

import 'dart:math' as math;

import 'package:flutter/material.dart';

class SpeedometerPainter extends CustomPainter {
  final double value;

  SpeedometerPainter({required this.value});

  @override
  void paint(Canvas canvas, Size size) {
    final center = Offset(size.width / 2, size.height);
    final radius = size.width / 2;
    const startAngle = -math.pi;
    const sweep = math.pi; // 180 degrees
    const segments = 100; // Increase for smoother gradient

    // Draw gradient dial
    for (int i = 0; i < segments; i++) {
      final segmentColor = Color.lerp(Colors.green, Colors.red, i / segments)!;
      final paintSegment = Paint()
        ..style = PaintingStyle.stroke
        ..strokeWidth = 50
        ..color = segmentColor;

      canvas.drawArc(
        Rect.fromCircle(center: center, radius: radius),
        startAngle + (sweep / segments) * i,
        sweep / segments,
        false,
        paintSegment,
      );
    }

    // Needle
    if (value > 0) {
      // Calculate the angle for the needle
      final needleAngle = startAngle + sweep * (value / 100);

      // Needle
      final paintNeedle = Paint()
        ..color = Colors.black // Color of the needle
        ..style = PaintingStyle.stroke
        ..strokeWidth = 4;

      // Draw needle
      canvas.drawLine(
        center,
        Offset(
          center.dx + radius * math.cos(needleAngle),
          center.dy + radius * math.sin(needleAngle),
        ),
        paintNeedle,
      );
    }
  }

  @override
  bool shouldRepaint(covariant CustomPainter oldDelegate) => true;
}
